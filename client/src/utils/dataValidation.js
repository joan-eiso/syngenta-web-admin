export const checkUndefinedKeyOnPayload = (payload) => {
  for (let key of Object.keys(payload)) {
      if(payload[key] === undefined ) return true;
  }
  return false;
}

export const checkEmptyValueOnPayload = (payload) => {
  for (let key of Object.keys(payload)) {
      if(payload[key].length === 0 ) return true;
  }
  return false;
}

export const checkValidEmail = (email) => {
  let isAValidEmail = emailRegex.test(email);
  return isAValidEmail;
}
